import Foundation
import UIKit

extension UIButton
{
    func setCorner()
    {
        //self.backgroundColor = .clear
        self.layer.cornerRadius = 6.66
        self.layer.borderWidth = 1
        self.layer.borderColor = UIColor.white.cgColor
        
    }
    
}
